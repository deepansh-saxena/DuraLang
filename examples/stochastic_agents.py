"""Fully stochastic multi-agent system — the LLM decides everything.

This is the example that proves DuraLang's value proposition.

The orchestrator LLM has tool schemas for three sub-agents. It freely decides:
  - Which agent to call
  - How many times to call each agent
  - What order to call them in
  - Whether to call the same agent again with refined questions
  - When it has enough information to respond

Each sub-agent also has its own tools and its own agent loop — the LLM
inside each sub-agent decides which tools to use and how many times to loop.

Agent-to-agent calls are routed as Temporal Child Workflows (not activities),
so there are no timeout issues and each agent gets its own event history.

This is impossible to express as a graph. The execution path is completely
determined at runtime by LLM outputs. But every single operation — every
LLM call, tool call, and agent call — is individually durable via Temporal.
"""

import asyncio
import json

from langchain_anthropic import ChatAnthropic
from langchain_core.messages import HumanMessage, SystemMessage, ToolMessage
from langchain_core.tools import tool

from duralang import dura

# ── Sub-agent tools ──────────────────────────────────────────────────────────


@tool
def web_search(query: str) -> str:
    """Search the web for current information on a topic."""
    return (
        f"Search results for '{query}':\n"
        f"1. Recent studies show significant developments in {query}.\n"
        f"2. Industry experts predict major shifts in {query} by 2027.\n"
        f"3. Investment in {query} grew 40% year-over-year."
    )


@tool
def wikipedia_lookup(topic: str) -> str:
    """Look up background and historical information on Wikipedia."""
    return (
        f"Wikipedia: {topic}\n"
        f"{topic} has roots in early computing research. The field has evolved "
        f"through several paradigm shifts. Current approaches emphasize "
        f"scalability, reliability, and autonomous operation."
    )


@tool
def calculator(expression: str) -> str:
    """Evaluate a mathematical expression. Use for quantitative analysis."""
    try:
        result = eval(expression, {"__builtins__": {}})  # noqa: S307
        return f"{expression} = {result}"
    except Exception as e:
        return f"Error evaluating '{expression}': {e}"


@tool
def format_document(title: str, body: str) -> str:
    """Format text into a clean document with title and body."""
    return f"# {title}\n\n{body}\n\n---\nGenerated by DuraLang stochastic agent system."


# ── Sub-agents (each is a @dura child workflow) ─────────────────────────────

research_tools = [web_search, wikipedia_lookup]
research_tools_by_name = {t.name: t for t in research_tools}


@dura
async def researcher(query: str) -> str:
    """Research agent with web search and wikipedia tools."""
    llm = ChatAnthropic(model="claude-sonnet-4-6")
    llm_with_tools = llm.bind_tools(research_tools)

    messages = [
        SystemMessage(
            content=(
                "You are a research agent. Use web_search for current info and "
                "wikipedia_lookup for background. Gather what's needed to answer "
                "the query, then return a clear summary. Be concise."
            )
        ),
        HumanMessage(content=query),
    ]

    for _ in range(10):
        response = await llm_with_tools.ainvoke(messages)
        messages.append(response)
        if not response.tool_calls:
            break
        for tc in response.tool_calls:
            result = await research_tools_by_name[tc["name"]].ainvoke(tc["args"])
            messages.append(ToolMessage(content=str(result), tool_call_id=tc["id"]))

    return response.content


analysis_tools = [calculator]
analysis_tools_by_name = {t.name: t for t in analysis_tools}


