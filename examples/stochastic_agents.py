# ── Agent tool schemas (given to the LLM so it can choose which to call) ─────

AGENT_TOOL_SCHEMAS = [
    {
        "name": "call_researcher",
        "description": (
            "Call the research agent to gather information on a topic. "
            "Use this when you need current information, background context, "
            "or factual data about something."
        ),
        "input_schema": {
            "type": "object",
            "properties": {
                "query": {
                    "type": "string",
                    "description": "The research query",
                }
            },
            "required": ["query"],
        },
    },
    {
        "name": "call_analyst",
        "description": (
            "Call the analysis agent to analyze data and answer quantitative questions. "
            "Use this when you have data that needs analysis, calculations, "
            "or trend identification."
        ),
        "input_schema": {
            "type": "object",
            "properties": {
                "data": {
                    "type": "string",
                    "description": "The data to analyze",
                },
                "question": {
                    "type": "string",
                    "description": "The analysis question",
                },
            },
            "required": ["data", "question"],
        },
    },
    {
        "name": "call_writer",
        "description": (
            "Call the writer agent to produce a formatted document. "
            "Use this when you have gathered enough information and analysis "
            "and want to produce a final polished output."
        ),
        "input_schema": {
            "type": "object",
            "properties": {
                "content": {
                    "type": "string",
                    "description": "The content to format",
                },
                "instructions": {
                    "type": "string",
                    "description": "How to format/write the content",
                },
            },
            "required": ["content", "instructions"],
        },
    },
]

# Map tool names to @dura functions — called directly from the orchestrator's
# workflow context, so they become Temporal Child Workflows (not activities).
AGENT_DISPATCH = {
    "call_researcher": lambda args: researcher(args["query"]),
    "call_analyst": lambda args: analyst(args["data"], args["question"]),
    "call_writer": lambda args: writer(args["content"], args["instructions"]),
}


# ── Orchestrator — LLM decides everything ────────────────────────────────────


@dura
async def orchestrator(task: str) -> str:
    """Fully stochastic orchestrator — the LLM decides which agents to call and when.

    Agent tool calls are dispatched as Child Workflows directly from
    the workflow context. The LLM freely chooses which agents to call,
    in what order, and how many times.
    """
    llm = ChatAnthropic(model="claude-sonnet-4-6")
    llm_with_tools = llm.bind_tools(AGENT_TOOL_SCHEMAS)

    messages = [
        SystemMessage(
            content=(
                "You are an orchestrator with access to three specialist agents:\n"
                "- call_researcher: gathers information (web search, wikipedia)\n"
                "- call_analyst: analyzes data (calculations, trends)\n"
                "- call_writer: produces formatted documents\n\n"
                "You decide which agents to call, in what order, and how many times. "
                "You can call the same agent multiple times with different queries. "
                "You can call agents in whatever order makes sense for the task.\n\n"
                "When you have enough information, provide your final answer directly."
            )
        ),
        HumanMessage(content=task),
    ]

    for i in range(20):  # safety limit
        response = await llm_with_tools.ainvoke(messages)
        messages.append(response)

        if not response.tool_calls:
            break

        for tc in response.tool_calls:
            agent_name = tc["name"]
            print(f"  → Orchestrator calling: {agent_name}")

            # Dispatch directly as a child workflow (not through dura__tool activity)
            dispatch_fn = AGENT_DISPATCH.get(agent_name)
            if dispatch_fn:
                result = await dispatch_fn(tc["args"])
            else:
                result = f"Unknown agent: {agent_name}"

            messages.append(
                ToolMessage(content=str(result), tool_call_id=tc["id"])
            )

    return response.content


# ── Run ──────────────────────────────────────────────────────────────────────


async def main():
    print()
    print("=" * 60)
    print("STOCHASTIC MULTI-AGENT SYSTEM")
    print("The LLM decides which agents to call and in what order.")
    print("Every run may take a different path.")
    print("=" * 60)
    print()

    result = await orchestrator(
        "Research the current state of AI agents in software engineering. "
        "Analyze the growth trends. Write a brief report."
    )

    print()
    print("=" * 60)
    print("FINAL OUTPUT")
    print("=" * 60)
    print(result)
    print("=" * 60)


if __name__ == "__main__":
    asyncio.run(main())
